    <p>Haller&rsquo;s outstanding status in eighteenth-century medicine is based primarily on his systematic study of the human body, using subjects both living and deceased. In anatomy, he emphasised the need for repeated sectioning as the only way of differentiating frequently occurring structures from rare varieties. The preparation of almost 400 cadavers allowed him to depict the course of the arteries in the human body to a degree not previously achieved &nbsp;(*<a href="hallerstexteimnetz.php#Iconesanat1743-56">Icones anat. 1743&ndash;56</a>). Other studies were concerned with abnormalities and their regularity. For many years Haller&rsquo;s name was associated with the anatomical structures of the diaphragm (arcus lumbocostales Halleri), the testicles (rete Halleri), and the vascular system (tripus Halleri).</p>
<p>For Haller, anatomy was the basis for researching life processes, and physiology was animated anatomy (antomia animata). The crucial and decisive research method, however, was experimentation on live subjects. Haller qualifies as the founder of experimental physiology by virtue of having systematically carried out many and frequently cruel animal experiments to assess the sensibility and irritability of individual parts of the body. As a result of his investigations (*<a href="hallerstexteimnetz.php#Departirrit1753">De part. irrit. 1753</a>) he attributed specific characteristics to specific organic structures (irritability to the muscles, sensibility to the nerves) and was thus able to distinguish between the previously unclearly differentiated realms of movement and sensation. This triggered a European-wide controversy which had a pronounced effect on medical concepts in the second half of the 18th Century.</p>
<p>Haller reviewed his own writings continually, and frequently published them in several revised editions. By combining his own research and his critical reception of the writings of others, Haller ultimately shaped his major critical syntheses in physiology (*<a href="hallerstexteimnetz.php#Boerhaaveprael1739-44">Boerhaave prael. 1739&ndash;44</a> [373], *<a href="hallerstexteimnetz.php#Primlinphysiol1747">Prim. lin. physiol. 1747</a> [390], *<a href="hallerstexteimnetz.php#Elphysiol1757-66">El. physiol. 1757&ndash;66</a> [423]). The latter work &ndash; the &ldquo;Elementa physiologiae&rdquo; in eight volumes &ndash; became the starting point for the further development of physiology in the 19th Century.</p>
<p>Haller undertook additional studies of blood flow, bone structure, and embryonic development (*<a href="hallerstexteimnetz.php#Deformationecordis1767">De formatione cordis 1767</a> [903]). Based on detailed studies of chicken eggs and on his own ideological convictions, Haller proposed that a nucleus did not develop as something new, but existed from the beginning of conception and simply grew to become visible (theory of preformation). In order to document the current state of knowledge and make it available to the scholarly world, Haller catalogued and annotated the entire body of medical writing in three bibliographies (*<a href="hallerstexteimnetz.php#Biblanat1774-77">Bibl. anat. 1774&ndash;77</a> [329], *<a href="hallerstexteimnetz.php#Biblchir1774-75">Bibl. chir. 1774&ndash;75</a> [1089], *<a href="hallerstexteimnetz.php#Biblmedpract1776-88">Bibl. med. pract. 1776&ndash;88</a> [1091]).</p>
<p>Haller&rsquo;s activity as a practicing physician was long underestimated. As his correspondence confirms, he not only practiced from 1729 to 1736 in Bern but also worked throughout his life as a consulting physician (particularly in complicated cases of illness). His name is associated with the &ldquo;Elixir acidum Halleri&rdquo;, which consists of one part sulphuric acid and three parts alcohol and was listed in the &ldquo;Pharmacopoea Helvetica Quarta&rdquo; (1907, valid until 1933).</p>
