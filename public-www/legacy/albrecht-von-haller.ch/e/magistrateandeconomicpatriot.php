    <p>Albrecht von Haller saw himself as a member of Bernese patrician society, who strove to use his knowledge and his scientific curiosity, as well as his critical judgment, in the service of the republic. This was the main reason that Haller left his post as a professor in G&ouml;ttingen and returned to Bern. The way for this move was paved while Haller was still abroad, when he was elected to the Great Council in Bern in 1745 with the support of his patron Isaak Steiger. As a member of the Great Council, he was eligible for a career as a magistrate, which he took up upon his return from G&ouml;ttingen in 1753 &ndash; first as head of the town hall (Rathausammann), school inspector (Schulrat), and orphanage administrator, and later as director of the salt works in Roche and public health official (Sanit&auml;tsrat). Haller&rsquo;s work as a magistrate &ndash; which focused on educational reform, education for industriousness, strategies for reducing vulnerability to crises (issues of medical administration, animal epidemics), and optimised use of local resources (salt, domestic plants) &ndash; covered the entire spectrum of the enlightened reform movement in Europe in the second half of the 18th Century, with which Haller had a great deal of interaction. His active role in the Economic Society of Bern, which he served as president for a number of years from 1766, was a direct consequence. His work for the Society included basic publications on native plant resources (*<a href="hallerstexteimnetz.php#Baeume1763">B&auml;ume 1763</a> [1258], *<a href="hallerstexteimnetz.php#Deplantispabularibus1771">De plantis pabularibus 1771</a> [1264], *<a href="hallerstexteimnetz.php#Generacerealium1775-76">Genera cerealium 1775&ndash;76</a> [1270]), a preface to a selection of treatises translated from Swedish (*<a href="hallerstexteimnetz.php#VorredeStaatswirtschaft1763">Vorrede Staatswirtschaft 1763</a> [1416]), a report on the draining of a parcel of land (*<a href="hallerstexteimnetz.php#Culture1764">Culture 1764</a> [1417]), and a treatise on combating cattle plague (*<a href="hallerstexteimnetz.php#Viehseuche1772">Viehseuche 1772</a> [1399]). Haller&rsquo;s position in the economic and patriotic reform movement and his activities as a magistrate must be considered together for an adequate interpretation, as he was simultaneously active as a scientific expert, administrative official, and government representative with the power to shape public life.</p>
