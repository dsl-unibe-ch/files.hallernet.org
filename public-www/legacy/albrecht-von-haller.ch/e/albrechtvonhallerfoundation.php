    <p>The Albrecht von Haller Foundation of the Burgergemeinde Bern was the organisation that initiated the arrangements for the anniversary year &ldquo;Haller 300&rdquo;. It is happy to note its success in attracting a large number of different partners and sponsors within very little time. This programme of the anniversary year attests to the diverse events that are being prepared to which we can look forward. The Albrecht von Haller Foundation expresses gratitude towards all its sponsors; it hopes that the events are met with the success they deserve and that those interested in Bern, Albrecht von Haller and his century find much to enjoy.</p>
<p>The Albrecht von Haller Foundation was established by the Grosse Burgerrat in 1977, on the occasion of the 200<sup>th</sup> anniversary of Haller&rsquo;s death. Its aims are the promotion and commendation of:</p>
<p>&ndash; research into Haller&rsquo;s person and his estate, which is kept by the Burgerbibliothek Bern;</p>
<p>&ndash; scientific research in the fields in which Haller himself was active.</p>
<p>The Foundation issues a book series, the Studia Halleriana.</p>
<p>Originally endowed with CHF 250&rsquo;000, the Foundation has benefited from bequests from a number of individuals. This allows it to perform its tasks better; however, future financial contributions are very welcome.</p>
