    <p>One of the most prominent scientists of his time, Haller not only carried out experiments and investigations but also reflected on the conditions, possibilities, and principles of research. Although he did not present his thoughts in comprehensive treatises, he did write brief contributions and expressed himself on this subject in numerous passages throughout his writings. Among the most important sources in this respect are his early reviews in the &ldquo;Biblioth&egrave;que raisonn&eacute;e&rdquo; and his essays on the benefits of learned societies and hypotheses (both published in *<a href="hallerstexteimnetz.php#KlSchriften1772">Kl. Schriften 1772</a> [7]).</p>
<p>Haller is often referred to as the last of the polymaths. He himself, however, did not regard encyclopaedic knowledge as a worthwhile goal. His scientific credo was specialised empirical research. In his view, knowledge would be furthered not by large-scale, comprehensive philosophical schemes but by experimental investigation in small and clearly defined areas of research. The decisive precondition for realising this aim was thorough knowledge of a specific field of study and its associated fields in order to have a grasp of the current state of knowledge and be able to define areas of research still open. Haller&rsquo;s major bibliographies in medicine and botany served this purpose. His wide-ranging knowledge was due not in the least to this contention and had little in common with conventional baroque scholarship.</p>
<p>Haller had a very pragmatic understanding of the scientific researcher, who in his view was driven substantially by ambition. He was of the opinion that research deserved recognition and honour, and that this could significantly accelerate progress. He also demanded that research not only be carried out by academics and privileged scholars &ndash; as was then the custom &ndash; but that it be institutionalised at universities. Haller&rsquo;s ideals had a substantial influence on the still new university of G&ouml;ttingen, which soon became a model for the future in German-speaking Europe.</p>
<p>Haller&rsquo;s ideas about the responsibilities and the position of the sciences have been insufficiently studied. Nor do we have a clear picture of his own working processes, be it in reading, reviewing and excerpting the work of others, in his own research, in his exchanges with fellow scholars, or in writing and revising his printed works. The rich amount of handwritten and printed material available makes it possible to carry out exemplary studies on the ideals and practices of an eighteenth-century scientist and scholar who was active in many different fields.</p>
