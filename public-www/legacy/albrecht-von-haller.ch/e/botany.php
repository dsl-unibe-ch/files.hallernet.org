    <p>Haller&rsquo;s botanical work includes comprehensive inventories (Flora) of fairly large regions (Switzerland, Jena, G&ouml;ttingen), monographs on plant species and families (e.g. European leeks and orchids), descriptions of botanical collecting expeditions, and a critically annotated bibliography (*<a href="hallerstexteimnetz.php#Biblbot1771-72">Bibl. bot. 1771&ndash;72</a> [1215]) of all botanical writing up to his time.</p>
<p>After several collecting expeditions primarily in the Alps and with the help of friends, Haller published the first comprehensive compendium of Swiss flora during his time as a professor in G&ouml;ttingen (*<a href="hallerstexteimnetz.php#Enumstirphelv1742">Enum. stirp. helv. 1742</a> [1189]). Of this volume, which was his principal botanical work, he published a second and considerably expanded edition in 1768 (*<a href="hallerstexteimnetz.php#Histstirphelv1768">Hist. stirp. helv. 1768</a> [1202]). At the University of G&ouml;ttingen he founded the botanical garden and developed and described its rich collection (*<a href="hallerstexteimnetz.php#EnumhortGott1753">Enum. hort. Gott. 1753</a> [1241]).</p>
<p>Haller was a pioneer in different branches of botany. He recognised variability among plants and rejected fixity of species; he undertook exact morphological descriptions of species based on repeated observation of many samples, including in different stages of development; and he recorded the locations where samples were found along with site-specific factors. His knowledge of plant geography and altitudinal belts is well-known. Detailed synonymy of old (pre-Linnean) plant names and an attempt to establish a natural system of plants are further particular features of Haller&rsquo;s botanical works. He published several of his minor botanical writings together in a volume (*<a href="hallerstexteimnetz.php#Opusculabot1749">Opuscula bot. 1749 </a>[1254]).</p>
<p>Haller rejected the binary nomenclature with its artificial sexual system successfully propagated by his contemporary, Carl von Linn&eacute;, on historical and scientific grounds. Accordingly, his names, for example for approximately 300 newly described flowering plants, are not valid, and his botanical work failed to have a major impact.</p>
<p>Haller compiled extensive herbaria which for the most part still exist today. The 60-volume main herbarium is located in the Mus&eacute;um national d&rsquo;Histoire naturelle in Paris, and a smaller one is kept at the Albrecht-von-Haller-Institut f&uuml;r Pflanzenwissenschaften at the University of G&ouml;ttingen. Several additional plant samples can be found in the herbarium of his son, Albrecht von Haller filius (1758&ndash;1823), at the Conservatoire et Jardin botaniques in Geneva.</p>
