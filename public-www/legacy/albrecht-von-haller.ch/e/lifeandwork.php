    <p>Haller&rsquo;s life and work are impressive in their abundance. The Bernese scholar wrote 24 monographs comprising 50 volumes. In addition to a small volume of poetry, his work consists of 3 utopian novels, 4 volumes on religious subjects, 10 devoted to botany, and 32 volumes concerned with medical topics. In addition, he wrote 52 brief (less than 10 pages) and 84 longer scientific treatises, 10 inaugural dissertations (defended by his students), 200 articles for medical encyclopaedias, 25 prefaces, and 80 book reviews in the form of essays. Altogether these writings amount to approximately 25,000 pages not counting revised editions, which Haller published in an additional 25 volumes. Not counted as well are 9000 reviews in the &ldquo;G&ouml;ttingische Gelehrte Anzeigen&rdquo; and 10 additional works in 52 volumes which he published himself.</p>
<p>Some clarification is necessary, however. It is true that Haller may be unequalled in his comprehensive overview of the learning and the thought of his time, in the extent of his reflections upon it and the abundance of his analyses and reviews, and perhaps also in his ability to mould it into a unified whole that was meaningful from his personal perspective. But as a productive researcher and scholar he was not himself engaged in what for his century would have been an uncommonly broad range of subjects. His central contributions are limited to the fields of poetry, literary criticism (reviews), medical science (particularly anatomy, physiology, and embryology), and botany, as well as certain specific areas of natural history, agriculture, and technology. Moreover, like many of his scholarly contemporaries, he was also active as a magistrate and engaged in an extensive exchange of letters.</p>
<p>The individual rubrics here provide information about Haller&rsquo;s work in these various fields. A detailed overview of Haller&rsquo;s life and work can be found in <a href="ab2005.php#SteinkeBoschungPross2008">Steinke/Boschung/Pross 2008</a>.<br />
The entire literature by and about Haller is indexed in the &ldquo;Bibliographia Halleriana&rdquo; (<a href="ab2005.php#SteinkeProfos2004">Steinke/Profos 2004</a>).</p>     </div>
