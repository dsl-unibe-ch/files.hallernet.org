    <p>Haller fut l&rsquo;un des scientifiques les plus &eacute;minents de son &eacute;poque, ne se contentant pas de r&eacute;aliser des exp&eacute;riences et d&rsquo;entreprendre des observations mais r&eacute;fl&eacute;chissant aussi aux conditions, possibilit&eacute;s et principes de la recherche. Il est vrai qu&rsquo;il n&rsquo;a pas expos&eacute; ses pens&eacute;es dans d&rsquo;importants trait&eacute;s, mais il a r&eacute;dig&eacute; de petites contributions et s&rsquo;est exprim&eacute; de mani&egrave;re ponctuelle en de nombreux endroits de son &oelig;uvre. Parmi les sources les plus importantes, on compte ses premi&egrave;res recensions pour la &laquo;&nbsp;Biblioth&egrave;que raisonn&eacute;e&nbsp;&raquo; ainsi que son m&eacute;moire sur l&rsquo;utilit&eacute; des Soci&eacute;t&eacute;s savantes et des hypoth&egrave;ses (tous publi&eacute;s dans *<a href="hallerstexteimnetz.php#KlSchriften1772">Kl. Schriften 1772</a> [7]).</p>
<p>Haller est souvent consid&eacute;r&eacute; comme le dernier g&eacute;nie universel. Lui pourtant, n&rsquo;a pas envisag&eacute; ce savoir encyclop&eacute;dique comme un but &agrave; rechercher absolument. Il a fait de la recherche empirique sp&eacute;cialis&eacute;e son credo scientifique, estimant que l&rsquo;accroissement des savoirs ne repose pas sur de vastes syst&egrave;mes th&eacute;oriques mais bien plut&ocirc;t sur l&rsquo;&eacute;tude exp&eacute;rimentale de petits champs de recherche clairement d&eacute;finis. La connaissance approfondie d&rsquo;une discipline et des domaines qui lui sont associ&eacute;s constitue une pr&eacute;misse d&eacute;cisive pour &eacute;tablir l&rsquo;&eacute;tat actuel des recherches et d&eacute;couvrir des champs d&rsquo;investigations originaux et prometteurs. Ses larges bibliographies r&eacute;pertoriant la litt&eacute;rature m&eacute;dicale et botaniste de son temps r&eacute;pondent &agrave; cette exigence. L&rsquo;&eacute;tendue et la diversit&eacute; du savoir de Haller trouvent leur fondement essentiellement dans ce programme de recherche et n&rsquo;ont pas grande chose en commun avec l&rsquo;&eacute;rudition baroque traditionnelle.</p>
<p>Haller avait une conception tr&egrave;s pragmatique du chercheur qui, &agrave; ses yeux, est motiv&eacute; principalement par son ambition. La reconnaissance et la r&eacute;tribution &eacute;taient selon lui les moteurs n&eacute;cessaires de la recherche, contribuant grandement &agrave; promouvoir l&rsquo;avancement des sciences. Il soutint par ailleurs une institutionnalisation de la recherche au sein des universit&eacute;s, &agrave; une &eacute;poque o&ugrave; celle-ci demeurait souvent encore le monopole des acad&eacute;mies et des chercheurs ind&eacute;pendants. Ses id&eacute;aux ont particip&eacute; &agrave; fa&ccedil;onner l&rsquo;Universit&eacute; de Gottingue, encore jeune, mais qui devait devenir bient&ocirc;t un mod&egrave;le novateur dans l&rsquo;espace germanophone.</p>
<p>Jusqu&rsquo;&agrave; pr&eacute;sent, la recherche n&rsquo;a accord&eacute; que peu d&rsquo;importance aux conceptions de la science, de ses fonctions et de sa place dans la pens&eacute;e hall&eacute;rienne. On regrette aussi l&rsquo;&eacute;tat lacunaire de nos connaissances de sa m&eacute;thode de travail et des diff&eacute;rentes &eacute;tapes qui la caract&eacute;risent &ndash; lecture, recension et d&eacute;pouillement de la litt&eacute;rature, suivis du d&eacute;veloppement d&rsquo;une recherche personnelle et de l&rsquo;&eacute;tablissement d&rsquo;&eacute;changes avec ses confr&egrave;res, et pour finir, r&eacute;daction, impression et remaniement des ouvrages publi&eacute;s. Les documents manuscrits et imprim&eacute;s que Haller nous a laiss&eacute;s constituent, de par leur richesse, un mat&eacute;riau de choix pour des &eacute;tudes consacr&eacute;es aux id&eacute;aux et pratiques d&rsquo;un chercheur et savant du XVIII&egrave;me si&egrave;cle qui se distingue par sa polyvalence.</p>
