  <div id="content">
    <p>L&rsquo;&oelig;uvre litt&eacute;raire de Haller est relativement succincte mais significative. C&rsquo;est tout particuli&egrave;rement le cas de son recueil de po&egrave;mes &laquo;&nbsp;Versuch Schweizerischer Gedichten&nbsp;&raquo; (*<a href="hallerstexteimnetz.php#Gedichte1732">Gedichte 1732</a>), qui planta un tout nouveau d&eacute;cor sur la sc&egrave;ne de la po&eacute;sie pastorale, amoureuse et didactique en langue allemande. Le po&egrave;me &laquo;&nbsp;Les Alpes&nbsp;&raquo; id&eacute;alise la puissance ainsi que la puret&eacute; de la Nature et des habitants du monde alpin qu&rsquo;il oppose &agrave; la population corrompue des villes. Il est le point de d&eacute;part d&rsquo;un enthousiasme grandissant pour la montagne et du tourisme qui lui est li&eacute;. D&rsquo;autres de ses po&egrave;mes se penchent sur des questions fondamentales de religion, d&rsquo;&eacute;thique et de m&eacute;taphysique (&laquo;&nbsp;Essai sur l&rsquo;origine du mal&nbsp;&raquo;, &laquo;&nbsp;Fragment d&rsquo;une Ode sur l&rsquo;Eternit&eacute;&nbsp;&raquo;). Admir&eacute;s par Klopstock et Herder, ils annoncent d&eacute;j&agrave; les po&egrave;mes philosophiques de Schiller (Gedankenlyrik).</p>
<p>Vers la fin de sa vie, Haller composa trois romans politiques dans lesquels il exposa diff&eacute;rents mod&egrave;les d&rsquo;ordres &eacute;tatiques inspir&eacute;s de l&rsquo;Histoire. Il d&eacute;peint ainsi l&rsquo;absolutisme &eacute;clair&eacute; dans &laquo;&nbsp;Usong, histoire orientale&nbsp;&raquo; (&laquo;&nbsp;Usong. Eine morgenl&auml;ndische Geschichte&nbsp;&raquo;, *<a href="hallerstexteimnetz.php#Usong1771">Usong 1771</a>), la monarchie constitutionnelle dans &laquo;&nbsp;Alfred, roi des Anglo-Saxons&nbsp;&raquo; (&laquo;&nbsp;Alfred, K&ouml;nig der Angelsachsen&nbsp;&raquo;, *Alfred 1773 XXX[212]) et enfin la r&eacute;publique oligarchique et aristocratique dans &laquo;&nbsp;Fabius et Cato. Fragment de l&rsquo;histoire romaine&nbsp;&raquo; (&laquo;&nbsp;Fabius und Cato. Ein St&uuml;ck der R&ouml;mischen Geschichte&nbsp;&raquo;, *<a href="hallerstexteimnetz.php#Fabius1774">Fabius 1774</a>).</p>
<p>En tant que repr&eacute;sentant et porte-parole d&rsquo;une foi en Dieu bas&eacute;e sur la raison, Haller r&eacute;digea plusieurs &eacute;crits dans lesquels il d&eacute;fendit la V&eacute;rit&eacute; de la r&eacute;v&eacute;lation biblique &ndash; en particulier contre les attaques de Voltaire (*<a href="hallerstexteimnetz.php#VorredeFormey1751">Vorrede Formey 1751</a> XXX[246], *<a href="hallerstexteimnetz.php#Offenbarung1772">Offenbarung 1772</a> XXX[265], *<a href="hallerstexteimnetz.php#Freygeister1775-77">Freygeister 1775-77</a>). La publication posthume d&rsquo;extraits de son journal intitul&eacute;e &laquo;&nbsp;Tagebuch religi&ouml;ser Empfindungen&nbsp;&raquo; (*<a href="hallerstexteimnetz.php#Tagebuch1787">Tagebuch 1787</a>) d&eacute;voile de fa&ccedil;on saisissante les souffrances existentielles auxquelles il fut confront&eacute; en qu&ecirc;te de la &laquo;&nbsp;vrai foi chr&eacute;tienne&nbsp;&raquo;.</p>
<p>Les r&eacute;cits de voyage, qu&rsquo;il &eacute;crivit pendant ses &eacute;tudes, pr&eacute;sentent quant &agrave; eux un int&eacute;r&ecirc;t pour la litt&eacute;rature et l&rsquo;histoire culturelle (*<a href="hallerstexteimnetz.php#TagebuchStudienreise1968">Tagebuch Studienreise 1968</a> [27], *<a href="hallerstexteimnetz.php#Tagebuecher1971">Tageb&uuml;cher 1971</a> [24]). Il publia enfin quelques m&eacute;moires philosophiques et litt&eacute;raires dans ses &oelig;uvres mineures (*<a href="hallerstexteimnetz.php#KlSchriften1772">Kl. Schriften 1772</a> [7]).</p>
