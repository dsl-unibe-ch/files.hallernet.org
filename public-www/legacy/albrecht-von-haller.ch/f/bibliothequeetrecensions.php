    <p>Albrecht von Haller poss&eacute;dait l&rsquo;une des plus grandes biblioth&egrave;ques de son temps. Apr&egrave;s sa mort, la plus grande partie de ses livres furent vendus au D&eacute;partement de m&eacute;decine et de sciences de la Biblioteca Braidense &agrave; Milan tandis que de nombreux autres furent achet&eacute;s par la Biblioth&egrave;que universitaire de Pavie. Aujourd&rsquo;hui, seules les mentions du possesseur, les notes manuscrites et l&rsquo;ex-libris permettent de reconstituer l&rsquo;unit&eacute; de cette collection &ndash; un important travail qui a &eacute;t&eacute; r&eacute;alis&eacute; sous la direction de Maria Teresa Monti. Gr&acirc;ce au catalogue de cette derni&egrave;re, nous poss&eacute;dons des informations d&eacute;taill&eacute;es sur les quelques 23'000 titres de ce fond (<a href="bis2004.php#Monti1983-94">Monti 1983-1994</a>). &Agrave; ce jour, on d&eacute;plore cependant l&rsquo;absence d&rsquo;&eacute;tudes approfondies analysant la formation de cette biblioth&egrave;que et sa signification pour le travail de Haller. Ce dernier avait d&eacute;j&agrave; &eacute;tabli son propre catalogue en 1735, qui recense 1'200 titres et a fait l&rsquo;objet d&rsquo;une premi&egrave;re analyse par Urs Boschung (<a href="bis2004.php#Boschung1995">Boschung 1995</a>). Une base de donn&eacute;es d&eacute;di&eacute;e &agrave; Albrecht von Haller contient l&rsquo;ensemble du catalogue de Monti, celui de 1735 et des parties du catalogue plus complet de 1757/1768 qui inventorie quelques 7'250 titres. Elle offre un point de d&eacute;part id&eacute;al pour des &eacute;tudes subs&eacute;quentes. Celles-ci devront aussi prendre en compte les dix volumes de bibliographies que Haller &eacute;tablit pour la m&eacute;decine et la botanique de son temps et dans lesquels les ouvrages issus de sa biblioth&egrave;que sont signal&eacute;s par un ast&eacute;risque (<a href="ab2005#Braun-Bucher2008">Braun-Bucher 2008</a>).</p>
<p>&Agrave; partir de 1728, Haller se mit &agrave; consigner dans sa &laquo;&nbsp;Iudicia librorum&nbsp;&raquo; manuscrite les critiques et r&eacute;sum&eacute;s de pr&egrave;s de 5'000 livres (cf. Burgerbibliothek Bern, Mss. Haller 32-54). Il r&eacute;digea en outre d&egrave;s 1737 des recensions pour diff&eacute;rents journaux, dont les G&ouml;ttingischen Gelehrten Anzeigen (GGA) pour lequel il effectua la plupart de ses recensions &agrave; partir de 1745. Celles-ci repr&eacute;sentent quelques 9'000 contributions. Ses recensions manuscrites n&rsquo;ont jamais &eacute;t&eacute; r&eacute;pertori&eacute;es. Pour ce qui est des GGA, seuls les examens d&rsquo;&oelig;uvres litt&eacute;raires ont &eacute;t&eacute; inventori&eacute;s (<a href="bis2004.php#Guthke1962">Guthke 1962</a>, <a href="ab2005.php#ProfosFrick2009">Profos Frick 2009</a>). Les recensions publi&eacute;es au sein d&rsquo;autres p&eacute;riodiques ont, elles, &eacute;t&eacute; r&eacute;f&eacute;renc&eacute;es dans la &laquo;&nbsp;Bibliographia Halleriana&nbsp;&raquo; (<a href="bis2004.php#SteinkeProfos2004">Steinke/Profos 2004</a>). Ses critiques d&rsquo;ouvrages des Belles-Lettres ont fait l&rsquo;objet d&rsquo;&eacute;tudes d&eacute;taill&eacute;es (<a href="bis2004.php#Guthke1962">Guthke 1962</a>, <a href="ab2005.php#ProfosFrick2009">Profos Frick 2009</a>), contrairement &agrave; ces recensions de textes scientifiques et &eacute;conomiques. On mentionnera pour finir une &eacute;tude qui a ouvert la voie de l&rsquo;analyse des interactions entre son activit&eacute; de critique et sa correspondance (<a href="ab2005.php#Stuber2004">Stuber 2004</a>).</p>     </div>
