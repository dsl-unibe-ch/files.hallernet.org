    <p>Un projet en cours, financ&eacute; par le Fonds national suisse (FNS) et dirig&eacute; par l&rsquo;Institut d&rsquo;histoire de l&rsquo;Universit&eacute; de Berne, s&rsquo;intitule &laquo;&nbsp;Science utile, appropriation de la nature et politique. La Soci&eacute;t&eacute; &eacute;conomique de Berne dans le contexte europ&eacute;en, 1750-1850&nbsp;&raquo; (&laquo;&nbsp;N&uuml;tzliche Wissenschaft, Naturaneignung und Politik. Die Oekonomische Gesellschaft Bern im europ&auml;ischen Kontext, 1750-1850&nbsp;&raquo;&nbsp;; <a href="http://www.oeg.hist.unibe.ch">www.oeg.hist.unibe.ch</a>). Il analyse le mouvement de r&eacute;forme lanc&eacute; par les patriotes &eacute;conomiques et interroge la naissance, la circulation, l&rsquo;&eacute;tablissement, la mise en pratique et le retentissement de nouveaux domaines de savoirs &laquo;&nbsp;utiles&nbsp;&raquo;. Albrecht von Haller est l&rsquo;un des acteurs caract&eacute;ristiques de la Soci&eacute;t&eacute; &eacute;conomique de Berne, tout &agrave; la fois experts scientifiques, employ&eacute;s de l&rsquo;administration et membres du gouvernement. Il ne peut donc faire l&rsquo;objet d&rsquo;une &eacute;tude singuli&egrave;re mais bien plut&ocirc;t comparative, en compagnie d&rsquo;autres magistrats savants tels que Samuel Engel, Daniel Fellenberg, Emanuel von Graffenfried von Burgistein, Niklaus Emanuel Tscharner et Vinzenz Bernhard Tscharner. <a href="mailto:%20martin.stuber@hist.unibe.ch">Martin Stuber</a></p>
<p>Le projet FNS &laquo;&nbsp;M&eacute;decin et patient autour de 1730&nbsp;&raquo; (Arzt und Patient um 1730) se consacre quant &agrave; lui &agrave; l&rsquo;analyse et &agrave; l&rsquo;&eacute;dition du journal d&eacute;taill&eacute; en latin que Haller tint tout au long de ses ann&eacute;es de m&eacute;decin praticien &agrave; Berne, alors qu&rsquo;il n&rsquo;avait pas encore 30 ans (1731-1736). Le projet part du constat qu&rsquo;en d&eacute;pit de tous les efforts d&eacute;ploy&eacute;s par l&rsquo;histoire sociale, nos connaissances concr&egrave;tes du quotidien m&eacute;dical des &eacute;poques pr&eacute;c&eacute;dentes sont rest&eacute;es bien lacunaires. Il r&egrave;gne une pareille obscurit&eacute; sur le cabinet m&eacute;dical, lieu pourtant central de l&rsquo;interaction entre le m&eacute;decin et le patient et t&eacute;moin de structures et processus sociaux fondamentaux. Le projet s&rsquo;attache &agrave; d&eacute;crire le cabinet aussi bien du point de vue du m&eacute;decin que de celui du patient. Par l&agrave;, il esp&egrave;re mettre en lumi&egrave;re les structures sociales et les comportements des patients, les maux pr&eacute;sent&eacute;s au m&eacute;decin, les mesures prises par ce dernier et enfin, les strat&eacute;gies de d&eacute;cisions et d&rsquo;actions. <a href="mailto:urs.boschung@mhi.unibe.ch">Urs Boschung</a></p>
<p>Un congr&egrave;s intitul&eacute; &laquo;&nbsp;Les pratiques du savoir et la figure du savant au XVIII&egrave;me si&egrave;cle&nbsp;&raquo; a &eacute;t&eacute; organis&eacute; en 2008 &agrave; l&rsquo;occasion du tricentenaire de la naissance de Haller. Son programme peut-&ecirc;tre consult&eacute; <a href="../medien/pdf/Praktiken_2008_Programm.pdf">ici</a>, et les r&eacute;sum&eacute;s de ses communications <a href="../medien/pdf/Praktiken_2008_abstracts.pdf">ici</a>. La publication de ses actes est en pr&eacute;paration. <a href="mailto:hubert.steinke@mhi.unibe.ch">Hubert Steinke</a></p>
<p>L&rsquo;une des caract&eacute;ristiques centrales de la science moderne et son caract&egrave;re processuel. La recherche scientifique ne se fait pas au travers d&rsquo;actes uniques et individuels de d&eacute;couverte de la v&eacute;rit&eacute; mais se place dans une continuit&eacute; d&rsquo;efforts et d&rsquo;interactions vari&eacute;es entre personnes et institutions. Gottingue est paradigmatique de cette dynamique nouvelle qui commence &agrave; s&rsquo;&eacute;tablir au XVIII&egrave;me si&egrave;cle et qui est encore constitutive pour la science actuelle. En effet, on y constate des interactions toutes particuli&egrave;res entre des r&eacute;seaux institutionnels &ndash; universit&eacute;, biblioth&egrave;que universitaire, G&ouml;ttingische Gelehrte Anzeigen (GGA), Soci&eacute;t&eacute; des sciences de Gottingue &ndash; et des r&eacute;seaux egocentr&eacute;s sur des acteurs c&eacute;l&egrave;bres tels que Gerlach Adolph von M&uuml;nchhausen, Christian Gottlieb Heyne et Albrecht von Haller.&nbsp;&Agrave; ce titre, un projet de recherche est en ce moment en pr&eacute;paration avec la collaboration de plusieurs institutions &agrave; Berne et Gottingue. <a href="mailto:martin.stuber@hist.unibe.ch">Martin Stuber</a></p>     </div>
