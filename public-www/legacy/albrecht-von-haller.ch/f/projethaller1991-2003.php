    <p>Officiellement intitul&eacute; &laquo;&nbsp;Albrecht von Haller et la R&eacute;publique des lettres du XVIII&egrave;me si&egrave;cle&nbsp;&raquo;, mais rapidement surnomm&eacute; le &laquo;&nbsp;Projet Haller bernois&nbsp;&raquo;, ce projet a &eacute;t&eacute; financ&eacute; par le Fonds national suisse (FNS), la Fondation Albrecht von Haller de la Commune Bourgeoise de Berne, la Fondation Silva Casa et la Soci&eacute;t&eacute; de Loterie SEVA. Il a &eacute;t&eacute; dirig&eacute; par Urs Boschung (Institut d&rsquo;histoire de la m&eacute;decine, Universit&eacute; de Berne) et r&eacute;alis&eacute; en coop&eacute;ration avec la Biblioth&egrave;que de la Bourgeoisie de Berne.</p>
<p>Le projet de recherche avait pour objectif (1) de cataloguer l&rsquo;ensemble de la correspondance de Haller, (2) d&rsquo;&eacute;tablir une bibliographie des sources et de la litt&eacute;rature secondaire, (3) d&rsquo;inventorier l&rsquo;int&eacute;gralit&eacute; du fonds manuscrit, et enfin (4) de dresser une chronologie de sa vie. En cours de route, il est apparu que le projet ne pourrait se passer d&rsquo;une importante base de donn&eacute;es. Il a par ailleurs sembl&eacute; opportun de se focaliser sur les deux premiers buts et surtout sur la vaste correspondance. Celle-ci a b&eacute;n&eacute;fici&eacute; de la r&eacute;orientation du projet et a pu &ecirc;tre inventori&eacute;e dans le <a href="bis2004.php#Repertorium2002">R&eacute;pertoire</a> de la correspondance de Albrecht von Haller avec plus d&rsquo;exactitude qu&rsquo;on n&rsquo;avait pu l&rsquo;esp&eacute;rer tout d&rsquo;abord &ndash; aussi bien au niveau de la forme que du contenu.<br />
La &laquo;&nbsp;Bibliographia Halleriana&nbsp;&raquo; (<a href="ab2005.php#SteinkeProfos2004">Steinke/Profos 2004</a>) est l&rsquo;aboutissement du second objectif. Elle est la premi&egrave;re &agrave; recenser de fa&ccedil;on exhaustive la litt&eacute;rature de et sur Haller. La saisie des quelques 9'000 recensions parues dans les &laquo;&nbsp;G&ouml;ttingischen Gelehrten Anzeigen&nbsp;&raquo; demeure en revanche un desiderata de la recherche.</p>
<p>La vaste base de donn&eacute;es a quant &agrave; elle permis non seulement de pr&eacute;parer la description de la correspondance au sein du Repertorium 2002 mais aussi de faire appara&icirc;tre les structures &eacute;l&eacute;mentaires du grand r&eacute;seau de Haller et les diff&eacute;rentes formes de communications qui y sont mises en jeu. De nombreux articles, tout comme le dernier volume &eacute;dit&eacute; par le groupe du projet bernois &laquo;&nbsp;Hallers Netz. Ein europ&auml;ischer Gelehrtenbriefwechsel zur Zeit der Aufkl&auml;rung&nbsp;&raquo; (<a href="ab2005.php#StuberHaechlerLienhard2005">Stuber/H&auml;chler/Lienhard 2005</a>), ont &eacute;t&eacute; consacr&eacute;s depuis &agrave; cette probl&eacute;matique.</p>     </div>
