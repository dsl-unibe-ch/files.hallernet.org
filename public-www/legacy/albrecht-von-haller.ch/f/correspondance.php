  <div id="content">
    <p>La majeure partie de la correspondance de Albrecht von Haller se trouve &agrave; la Biblioth&egrave;que de la Bourgeoisie &agrave; Berne. Le <a href="bis2004.php#Repertorium2002">Repertorium 2002</a> r&eacute;pertorie l&rsquo;int&eacute;gralit&eacute; des lettres connues, accompagn&eacute;es du lieu dans lequel elles sont conserv&eacute;es et de leurs &eacute;ventuelles &eacute;ditions. Outre celles qui appartiennent au fond de la Biblioth&egrave;que de la Bourgeoisie, le r&eacute;pertoire &eacute;num&egrave;re les lettres d&eacute;pos&eacute;es dans plus de 400 archives et biblioth&egrave;ques d&rsquo;Europe et d&rsquo;outre-mer, ainsi que celles adress&eacute;es par et &agrave; Haller, qui n&rsquo;ont pas surv&eacute;cu au temps mais sont mentionn&eacute;es dans diff&eacute;rents inventaires et publications. On y trouve aussi des informations d&eacute;taill&eacute;es sur les crit&egrave;res de classification des lettres, des r&eacute;sum&eacute;s de chaque &eacute;change &eacute;pistolaire, les biographies succinctes de chaque correspondant ainsi qu&rsquo;une table chronologique et g&eacute;ographique de la correspondance. On recense au total 16'981 lettres de 1'139 correspondants, 50 correspondantes et 11 institutions. Les lettres adress&eacute;es &agrave; Haller ont &eacute;t&eacute; mieux conserv&eacute;es que celles qu&rsquo;il a lui-m&ecirc;me &eacute;crites&nbsp;; le nombre de ces derni&egrave;res s&rsquo;&eacute;l&egrave;ve &agrave; 3'744 lettres, sa correspondance passive en comportant 13'237.</p>
<p>&Agrave; ce jour, il n&rsquo;existe pas d&rsquo;&eacute;dition compl&egrave;te de la correspondance de Haller; seul un tiers de ces lettres sont publi&eacute;es. Haller a fait para&icirc;tre lui-m&ecirc;me une s&eacute;lection d&rsquo;&eacute;p&icirc;tres qui lui furent adress&eacute;es (*<a href="hallerstexteimnetz.php#DtBriefe1777">Dt. Briefe 1777</a> [1430], *<a href="hallerstexteimnetz.php#EpistLat1773-75">Epist. Lat. 1773-75</a> [1431]). Certains de ses &eacute;changes &eacute;pistolaires les plus importants ont fait l&rsquo;objet d&rsquo;une &eacute;dition int&eacute;grale. On poss&egrave;de ainsi ses correspondances avec Charles Bonnet (<a href="bis2004.php#Sonntag1983">Sonntag 1983</a>), Christian Gottlob Heyne (<a href="bis2004.php#Dougherty1997">Dougherty 1997</a>), John Pringle (<a href="bis2004.php#Sonntag1999">Sonntag 1999</a>), Horace-B&eacute;n&eacute;dict de Saussure (<a href="bis2004.php#Sonntag1990">Sonntag 1990</a>), Ignazio Somis (<a href="bis2004.php#Hintzsche1965">Hintzsche 1965</a>, <a href="bis2004.php#Pagnamenta1969">Pagnamenta 1969</a> et <a href="bis2004.php#Realini1968">Realini 1968</a>. Jakob Reinbold Spielmann (<a href="bis2004.php#Vetter1986">Vetter 1986</a>), Johann Jakob Trew (<a href="bis2004.php#Steinke1999">Steinke 1999</a>) et Johann Georg Zimmermann (<a href="bis2004.php#Ischer1903-11">Ischer 1903-11</a> et *<a href="hallerstexteimnetz.php#Bodemann1885">Bodemann 1885</a> [18]). Quelques &eacute;ditions d&rsquo;autres grandes correspondances se sont content&eacute;es de publier les lettres de Haller en r&eacute;ponse &agrave; celles qui se trouvaient d&eacute;j&agrave; dans *<a href="hallerstexteimnetz.php#EpistLat1773-75">Epist. Lat. 1773-75</a> [1431]. Il en va ainsi des &eacute;changes &eacute;pistolaires avec Leopoldo Marcantonio Caldani (<a href="bis2004.php#Hintzsche1966">Hintzsche 1966</a>), Johannes Gessner (<a href="bis2004.php#Sigerist1923">Sigerist 1923</a>) et Giambattista Morgagni (<a href="bis2004.php#Hintzsche1964">Hintzsche 1964</a>). C&rsquo;est aussi le cas de la correspondance avec Samuel-August Tissot qui ne contient que les lettres de Haller (<a href="bis2004.php#Hintzsche1977">Hintzsche 1977</a>).</p>
<p>Un recueil (<a href="ab2005.php#StuberHaechlerLienhard2005">Stuber/H&auml;chler/Lienhard 2005</a>) rassemble plusieurs &eacute;tudes th&eacute;matiques consacr&eacute;es aux structures et aux pratiques en vigueur au sein du r&eacute;seau de Haller ainsi qu&rsquo;une analyse g&eacute;n&eacute;rale et comparative. Ce volume a &eacute;t&eacute; pr&eacute;c&eacute;d&eacute; par des &eacute;tudes sp&eacute;cifiques se concentrant sur des aspects singuliers de la correspondance de Haller. Parmi ces derni&egrave;res, il faut mentionner des travaux consacr&eacute;s au type de relation dominant qu&rsquo;est l&rsquo;&laquo;&nbsp;amiti&eacute; utile&nbsp;&raquo; (<a href="bis2004.php#Steinke1999">Steinke 1999</a>), aux particularit&eacute;s du pan bernois de la correspondance de Haller (<a href="bis2004.php#StuberHaechler2000">Stuber/H&auml;chler 2000</a>), aux interactions entre deux r&eacute;seaux de correspondances (<a href="bis2004.php#Haechler2002">H&auml;chler 2002</a>) et enfin aux relations qu&rsquo;entretiennent entre elles mobilit&eacute; g&eacute;ographique et &eacute;tablissement d&rsquo;un r&eacute;seau de correspondances (<a href="ab2005.php#Stuber2005b">Stuber 2005b</a>). Trois publications r&eacute;centes tentent par ailleurs de replacer Haller et ses strat&eacute;gies de communication dans le contexte plus large de la R&eacute;publique des lettres, que ce soit en empruntant les m&eacute;thodes d&rsquo;analyse des r&eacute;seaux (<a href="ab2005.php#Stuberetal2008">Stuber et al. 2008</a>), des transferts culturels (<a href="ab2005.php#Catherine2009">Catherine 2009</a>) ou en se demandant si les Lumi&egrave;res ne peuvent &ecirc;tre comprises en tant que mouvement au sein de la R&eacute;publique des lettres, s&rsquo;inscrivant entre la R&eacute;formation et la fin du XVIII&egrave;me si&egrave;cle (<a href="ab2005.php#SteinkeStuber2008">Steinke/Stuber 2008</a>).</p>
