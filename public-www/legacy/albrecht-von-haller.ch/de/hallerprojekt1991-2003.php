    <p>Das sogenannte &laquo;Berner Haller-Projekt&raquo; trug den offiziellen Titel &laquo;Albrecht von Haller und die Gelehtenrepublik des 18. Jahrhunderts&raquo; und wurde finanziert vom Schweizerischen Nationalfonds, von der Albrecht von Haller-Stiftung der Burgergemeinde Bern, Der Silva-Casa-Stiftung und der Seva Lotteriegenossenschaft. Es stand unter der Leitung von Urs Boschung (Institut f&uuml;r Medizingeschichte, Universit&auml;t Bern) und wurde in Zusammenarbeit mit der Burgerbibliothek Bern durchgef&uuml;hrt.</p>
<p>Das Forschungsprojekt hatte zum Ziel, 1. Hallers gesamte Korrespondenz zu verzeichnen, 2. eine Bibliographie der Prim&auml;r- und Sekund&auml;rliteratur zu erstellen, 3. den gesamten Nachlass detailliert zu inventarisieren und 4. sein Leben chronologisch zu dokumentieren. Im Laufe des Projekts dr&auml;ngte sich der Aufbau einer umfassenden Datenbank und die Fokussierung auf die ersten beiden Ziele und insbesondere den umfangreichen Briefwechsel auf. Dank dieser Konzentrierung konnte die Korrespondenz genauer als urspr&uuml;nglich gehofft formal und inhaltlich im <a href="bis2004.php#Repertorium2002">Repertorium</a> zu Albrecht von Hallers Korrespondenz verzeichnet werden. Die &laquo;Bibliographia Halleriana&raquo; (<a href="ab2005.php#SteinkeProfos2004">Steinke/Profos 2004</a>) dokumentierte erstmals und umfassend die Literatur von und &uuml;ber Haller, wenn auch die Erfassung seiner rund 9000 in den &laquo;G&ouml;ttingischen Gelehrten Anzeigen&raquo; ver&ouml;ffentlichten Buchrezensionen weiterhin ein Desiderat bleibt. Die breit angelegte Datenbank erm&ouml;glichte es, Hallers Briefwechsel nicht nur im Repertorium 2002 beschreibend zu erfassen, sondern auch die Grundstruktur des grossen Netzes und die sich darin abspielenden verschiedenen Formen von Kommunikationsprozessen zu untersuchen. Dieser Fragestellung widmeten sich zahlreiche Artikel sowie der letzte von der Berner Projekt-Gruppe herausgegebene Band &laquo;Hallers Netz. Ein europ&auml;ischer Gelehrtenbriefwechsel zur Zeit der Aufkl&auml;rung&raquo;, (<a href="ab2005.php#StuberHaechlerLienhard2005">Stuber/H&auml;chler/Lienhard 2005</a>).</p>     </div>
