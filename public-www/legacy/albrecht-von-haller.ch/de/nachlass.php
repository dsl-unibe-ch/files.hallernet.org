    <p>Hallers handschriftlicher Nachlass ist ausserordentlich reich und vielseitig. Er umfasst rund 150 B&auml;nde Manuskripte, 17&lsquo;000 Briefe und zwei Herbarien. Die Briefe und der gr&ouml;ssere Teil der Manuskripte befinden sich in der Burgerbibliothek Bern. Weitere Handschriftenb&auml;nde sind in der Biblioteca Nazionale Braidense in Mailand, wo ebenfalls Hallers grosse Bibliothek von 23&lsquo;000 Titeln lagert. Das grosse, 60b&auml;ndige Herbar wird im Mus&eacute;um national d&rsquo;Histoire Naturelle in Paris aufbewahrt, ein kleineres Herbar lagert im Albrecht-von-Haller-Institut f&uuml;r Pflanzenwissenschaften der Universit&auml;t G&ouml;ttingen.</p>
<p>Der Nachlass bietet sich als vorz&uuml;gliches Studienmaterial f&uuml;r die neuere Wissenschafts- und Kulturgeschichte an, die sich f&uuml;r die Gewinnung und die Praktiken des Wissens interessiert. In Verbindung mit der ebenfalls sehr reichen und gut dokumentierten Bibliothek und den zahlreichen Publikationen Hallers, liesse sich hier auf sehr genaue Weise und im grossen Umfang der ganze Forschungsprozess vom Lesen, Rezensieren und Exzerpieren &uuml;ber das eigene Forschen und den Austausch mit anderen Wissenschaftlern bis hin zum Verfassen und &Uuml;berarbeiten der gedruckten Werke nachzeichnen. Das reiche sowohl handschriftliche wie gedruckte Material erm&ouml;glicht exemplarische Studien &uuml;ber die Charakteristika experimenteller und gelehrter Forschung und &uuml;ber deren Interaktion. Bislang wurde vor allem die <a href="korrespondenz.php">Korrespondenz</a> ausf&uuml;hrlicher untersucht.</p>
<p>Ein kurzes Verzeichnis des handschriftlichen Nachlasses in Bern und  Mailand finden Sie <a href="../medien/pdf/Nachlass.pdf">hier</a>, eine Liste von Hallers  Korrespondenten <a href="../medien/pdf/Korrespondenten.pdf">hier</a>.</p>
<p>Inventare/Beschreibungen: Boschung 1974a (Kurzinventar der Berner Handschriften), Margez/Aupic/Lamy 2005 (Pariser Herbar), Monti 1983-94 (Katalog von Hallers Bibliothek, Nachtr&auml;ge zu Pecorello Vergnano 1965), Pecorello Vergnano 1965 (Inventar der Mail&auml;nder Handschriften), Repertorium 2002 (Verzeichnis des Briefwechsels), Braun 2008 (Beschreibung des gesamten Nachlasses), Zoller 1958 (Herbar).</p>
<p><strong>Bestellungen:</strong></p>
<p>Einzelne Handschriften und Briefe k&ouml;nnen als digitale Kopien bei der  Burgerbibliothek Bern bestellt werden. Bitte beachten Sie die  <a href="http://www.burgerbib.ch/d/portrait/benutzung.html" target="_blank">Benutzungsordnung</a>.  Bei Fragen wenden Sie sich an <a href="mailto:thomas.schmid@burgerbib.ch">Thomas Schmid</a>.</p>     </div>
