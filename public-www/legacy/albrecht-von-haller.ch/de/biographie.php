    <h2>Jugend, Studium</h2>
<p>Haller wurde 1708 als f&uuml;nftes Kind des Juristen Niklaus Emanuel Haller in Bern geboren. Er studierte Medizin in T&uuml;bingen und insbesondere in Leiden unter Herman Boerhaave, dem f&uuml;hrenden medizinischen Lehrer seiner Zeit. Nach der Promotion bereiste er England, vertiefte seine anatomischen und chirurgischen Kenntnisse in Paris und liess sich von Johannes Bernoulli in Basel in die h&ouml;here Mathematik einf&uuml;hren. 1729&ndash;36 war er als praktischer Arzt in Bern t&auml;tig und publizierte erste kleinere anatomische und botanische Schriften. Seinen fr&uuml;hen Ruhm jedoch erlangte er mit seinem <span class="kursiv">Versuch Schweizerischer Gedichten</span> (1732). Dieses kleine B&uuml;chlein enthielt ein tief empfundenes Liebesgedicht (<span class="kursiv">Doris</span>), zeigte neue Wege in der Beschreibung von Natur und Mensch (<span class="kursiv">Die Alpen</span>), wurde zum Vorbild einer philosophischen Lehrdichtung (<span class="kursiv">&Uuml;ber Vernunft, Aberglauben und Unglauben</span>) f&uuml;r die n&auml;chste Generation und machte Haller zum meistgelesenen deutschen Dichter der 1730er und 1740er Jahre.<br />
&nbsp;</p>
<h2>Professor in G&ouml;ttingen</h2>
<p>1736 wurde Haller zum Professor f&uuml;r Anatomie, Botanik und Chirurgie an die neugegr&uuml;ndete Universit&auml;t G&ouml;ttingen berufen, wo er bis 1753 blieb. In dieser Periode intensiver wissenschaftlicher Aktivit&auml;t entwickelte er seine zentralen Forschungsgebiete und legte die Grundlagen f&uuml;r sp&auml;tere Werke. 1742 ver&ouml;ffentlichte er eine umfassende Flora der Schweiz und galt bald als einer der f&uuml;hrenden Botaniker und als der wichtigste Gegner der aufkommenden Linn&eacute;schen Nomenklatur. Als Anatom fokussierte er sich auf das Gef&auml;sssystem und setzte mit seinen <span class="kursiv">Icones anatomicae</span> (8 Teile, 1743&ndash;56) den Standard in diesem Gebiet. Sein Hauptinteresse galt allerdings der Physiologie, der Lehre von den Funktionen der Lebewesen. Ausgehend von der &Uuml;berzeugung, dass sich fundamentale Aussagen &uuml;ber den lebenden K&ouml;rper an diesem selbst gewinnen lassen k&ouml;nnen, machte sich Haller in den sp&auml;ten 1740er Jahren als Erster daran, systematisch angeordnete Tierversuchsreihen in gr&ouml;sserem Umfang und mit klarer Fragestellung durchzuf&uuml;hren. Seine Resultate stellten die damalige Medizin auf den Kopf. Sie wiesen nach, dass der K&ouml;rper nicht &ndash; wie bisher angenommen &ndash; eine von der Seele geleitete, passive Maschine, sondern ein aktiver Organismus ist, der auf Reize reagiert. Dadurch ver&auml;nderte sich nicht nur die Vorstellung, was Leben &uuml;berhaupt ist, sondern auch, wie Krankheit entsteht. W&auml;hrend man vor 1750 davon ausging, dass Krankheit im Wesentlichen durch St&ouml;rungen der Fasern und S&auml;fte der K&ouml;rpermaschine bedingt ist, setzte sich nun die Meinung durch, dass eine gest&ouml;rte Reizbarkeit und Empfindsamkeit die Ursache allen &Uuml;bels ist.</p>
<p>In Anerkennung seiner wissenschaftlichen Leistungen wurde Haller 1749 vom Kaiser in den Adelsstand (von Haller) erhoben. Bedeutender noch war die Aufnahme in die wichtigsten europ&auml;ischen gelehrten Gesellschaften und Akademien, so in diejenigen von Uppsala (1733), London (1739), Stockholm (1747), Berlin (1749), Bologna (1751) und Paris (1754). 1751 wurde er zum Pr&auml;sident auf Lebenszeit der neugegr&uuml;ndeten G&ouml;ttinger <span class="kursiv">Gesellschaft der Wissenschaften</span> ernannt. Neben seinen zahlreichen wissenschaftlichen und literarischen T&auml;tigkeiten war Haller Chefredaktor der <span class="kursiv">G&ouml;ttingischen Gelehrten Anzeigen</span>, des f&uuml;hrenden deutschsprachigen Rezensionsjournal, f&uuml;r das er in den Jahren 1747&ndash;77 gut 9 000 Buchrezensionen verfasste.<br />
&nbsp;</p>
<h2>Magistrat in Bern</h2>
<p>Trotz der hohen internationalen Anerkennung als Wissenschaftler wurde Haller in G&ouml;ttingen nicht recht gl&uuml;cklich. Der Verlust von zwei Ehefrauen und von drei fr&uuml;h verstorbenen Kindern, der Streit mit Universit&auml;tskollegen und die Ferne von seinen Berner Freunden machten ihm zu schaffen. In der Hoffnung auf eine politische Karriere und um die politisch-gesellschaftliche Zukunft seiner Familie in Bern zu sichern, kehrte Haller 1753 in seine Heimat zur&uuml;ck (schon 1745 war er in den Grossen Rat gew&auml;hlt worden). Nach einigen Jahren in der bescheidenen, aber den Einstieg in h&ouml;here Posten ebnenden Position des Rathausammanns wurde Haller zum Salzdirektor in Roche im franz&ouml;sischen Teil des Berner Territoriums gew&auml;hlt. 1764 kehrte er nach Bern zur&uuml;ck und war ein wichtiges Mitglied in verschiedenen politischen Gremien wie der Landes&ouml;konomie-Kommission und dem Sanit&auml;tsrat. Dabei wurde er in seiner Mehrfachrolle als &sbquo;aufgekl&auml;rter&rsquo; Magistrat, als Verfasser von grundlegenden Abhandlungen (Futterkr&auml;uter, Getreide, Viehseuche) und als Pr&auml;sident der Oekonomischen Gesellschaft Bern (1766, 1768, 1770&ndash;1777) zu einer der zentralen Figuren der &ouml;konomisch-patriotischen Reformbewegung in Bern.<br />
&nbsp;</p>
<h2>Sp&auml;twerk</h2>
<p>Hallers R&uuml;ckkehr in die Schweiz war kein R&uuml;ckzug aus der Gelehrtenrepublik. Er unterhielt weiterhin einen ausgedehnten Briefwechsel mit Pers&ouml;nlichkeiten aus ganz Europa, von dem sich 3 700 Briefe an und 13 300 Briefe von 1200 Personen erhalten haben. Auch seine wissenschaftliche T&auml;tigkeit f&uuml;hrte er weiter. Die bereits in G&ouml;ttingen begonnenen embryologischen Forschungen intensivierte er und ver&ouml;ffentlichte seine Hauptwerke &uuml;ber die Entwicklung des H&uuml;hnerembryos 1758 und 1767. Sein <span class="kursiv">opus magnum</span>, die Elementa physiologiae, erschienen in 8 B&auml;nden &uuml;ber einen Zeitraum von 10 Jahren (1757&ndash;66) und blieben bis ins 19. Jahrhundert das Standardwerk des Fachs. Seine Sicht &uuml;ber Anatomie und Physiologie stellte er einem breiteren Publikum in 200 Artikeln der Yverdoner und den Supplementb&auml;nden zur Pariser <span class="kursiv">Encylop&eacute;die</span> (1772&ndash;77) vor. Eine zweite, stark erweiterte Ausgabe seiner Schweizer Flora publizierte er 1768. Abseits von den grossen Zentren der gelehrten Welt baute er seine grosse Bibliothek mit &uuml;ber 23 000 Titeln zur Medizin, Botanik und den Naturwissenschaften aus.</p>
<p>Das letzte Jahrzehnt seines Lebens widmete Haller unter anderem der Edition kritischer Bibliographien der Botanik, Anatomie, Physiologie, Chirurgie und praktischen Medizin. In 10 B&auml;nden pr&auml;sentierte und kommentierte er 50 000 Werke aus allen Bereichen der Medizin. Zudem schrieb er drei Romane &uuml;ber die verschiedenen Staatsformen und religi&ouml;se Schriften gegen die Freidenker, insbesondere Voltaire. Haller erlebte vielleicht die gr&ouml;sste Genugtuung seines Lebens im Juli 1777, ein halbes Jahr vor seinem Tod, als Kaiser Joseph II. es auf seiner 'inkognito'-Reise durch Europa ablehnte, Voltaire in Ferney zu besuchen, aber den Berner Gelehrten in seiner Stube aufsuchte.<br />
&nbsp;</p>
