  <div id="content">
    <p>Hallers grosse Wirkung in verschiedenen Bereichen der Wissenschaft, Gesellschaft und Kultur, seine unz&auml;hligen Publikationen und der reiche handschriftliche Nachlass haben Anlass zu vielen unterschiedlichen Untersuchungen gegeben. Dennoch ist Haller bei weitem nicht umfassend erforscht. Dies liegt sowohl am Umfang seines Oeuvres wie an der Vielgestaltigkeit seines Wesens und Wirkens. Haller zu erforschen, bedeutet weiterhin, sich auf ein schwieriges Terrain zu begeben.</p>
<p>Die Schwierigkeiten sind allerdings ein Argument, das nicht gegen, sondern f&uuml;r eine verst&auml;rkte Besch&auml;ftigung mit Haller spricht. Denn hinter ihnen steht die Vielgestaltigkeit und Widerspr&uuml;chlichkeit, die das Jahrhundert der Aufkl&auml;rung nebst eindeutigeren Programmen und Entwicklungslinien auch charakterisiert. Dies zeigen gerade die Resultate der j&uuml;ngeren Aufkl&auml;rungsforschung, die sich vermehrt mit der Verschr&auml;nkung der verschiedenen Lebensbereiche sowie mit historisch, wirtschaftlich, politisch, geographisch und konfessionell bedingten Unterschieden im 18. Jahrhundert besch&auml;ftigt. Dabei wird nicht nur nach Inhalten und Ideologien gefragt, sondern verst&auml;rkt nun auch nach Strukturen und Praktiken, die das wissenschaftliche, kulturelle und &ouml;konomische Leben erm&ouml;glichen und mitbestimmen.</p>
<p>Gerade f&uuml;r diese Fragestellungen ist das Beispiel Haller &auml;usserst ergiebig. Sein Leben und Wirken spielte sich nicht nur in unterschiedlichen geographischen R&auml;umen (Bern, G&ouml;ttingen), Strukturen (Universit&auml;t, Gelehrtenrepublik, Verwaltung) und auf verschiedenen Ebenen (Arzt, Dichter, Forscher, Magistrat) ab, es ist auch ausserordentlich umfangreich und genau dokumentiert. Unz&auml;hlige Briefe, Akten, Handschriften und Publikationen erm&ouml;glichen den Blick auf Vorder- und Hintergr&uuml;ndiges, auf Prozesse und Strukturen, auf Ideen und Ideologien und auf Statik und Dynamik im 18. Jahrhundert.</p>
<p>Die Voraussetzungen, sich genauer mit Haller zu besch&auml;ftigen, sind heute g&uuml;nstig. Einige grundlegende Arbeiten zu Hallers Denken, Dichtung und Medizin sowie die Edition mehrerer wichtiger Briefwechsel haben die Haller-Forschung in den letzten Jahrzehnten auf eine neue Stufe gehoben. Im Rahmen des Berner Haller-Projekts wurde die Literatur von und &uuml;ber Haller sowie sein ganzes Korrespondenznetz verzeichnet und analysiert. Der Zugang zu Haller wird zudem erleichtert durch eine Datenbank, die eine grosse und st&auml;ndig zunehmende Menge an Texten und Informationen umfasst.</p>     </div>
