    <p>Albrecht von Haller verstand sich als Angeh&ouml;riger des Berner Patriziats, der sein Wissen und seinen Forscherdrang, aber auch sein kritisches Urteilsverm&ouml;gen in den Dienst der Republik zu stellen suchte. Dies war ein Hauptgrund f&uuml;r Hallers Abschied von der G&ouml;ttinger Professur und die R&uuml;ckkehr nach Bern. Die Voraussetzungen dazu hatte er sich mit der Wahl in den Berner Grossen Rat geschaffen, die er 1745 aus der Ferne mit der Unterst&uuml;tzung seines Patrons Isaak Steiger erreichte. Als Angeh&ouml;riger des Grossen Rats stand Haller die Karriere als Magistrat offen, die er nach seiner R&uuml;ckkehr aus G&ouml;ttingen 1753 beschritt, zuerst als Rathausammann, Schulrat und Waisenhausverwalter, sp&auml;ter als Direktor der Salinen in Roche und als Sanit&auml;tsrat. Inhaltlich umfasste Hallers Magistratst&auml;tigkeit mit den Bildungsreformen, der Erziehung zur Arbeitsamkeit, den Strategien zur Minderung der Krisenanf&auml;lligkeit (Medizinalwesen, Viehseuchen) und zur optimierten Nutzung einheimischer Ressourcen (Salz, Nutzpflanzen) die ganze Palette der aufgekl&auml;rten Reformbewegung, wie sie sich in der zweiten H&auml;lfte des 18. Jahrhundert auf europ&auml;ischer Ebene pr&auml;sentierte und mit der Haller in vielf&auml;ltigem Austausch stand. Hallers aktive Rolle in der Oekonomischen Gesellschaft Bern, die er seit 1766 mehrere Jahre pr&auml;sidierte, ist daher nichts als folgerichtig. F&uuml;r die Berner Soziet&auml;t ver&ouml;ffentlichte Haller Grundlegendes zu den einheimischen Pflanzenressourcen (*<a href="hallerstexteimnetz.php#Baeume1763">B&auml;ume 1763</a> [1258], *<a href="hallerstexteimnetz.php#Deplantispabularibus1771">De plantis pabularibus 1771</a> [1264], *<a href="hallerstexteimnetz.php#Generacerealium1775-76">Genera cerealium 1775-76</a> [1270]), eine Vorrede zu einer Auswahl &uuml;bersetzter schwedischer Abhandlungen (*<a href="hallerstexteimnetz.php#VorredeStaatswirtschaft1763">Vorrede Staatswirtschaft 1763</a> [1416]), einen Erfahrungsbericht zur Entsumpfung eines Landst&uuml;ckes (*<a href="hallerstexteimnetz.php#Culture1764">Culture 1764</a> [1417]) und eine Abhandlung zur Bek&auml;mpfung der Viehseuche (*<a href="hallerstexteimnetz.php#Viehseuche1772">Viehseuche 1772</a> [1399]). Um angemessen interpretiert zu werden, m&uuml;ssen Hallers Stellung in der &ouml;konomisch-patriotischen Reformbewegung und seine Magistratst&auml;tigkeit gemeinsam in den Blick genommen werden, agierte er doch gleichzeitig als wissenschaftlicher Experte, als ausf&uuml;hrender Mann der Verwaltung und als gestaltender Akteur mit Regierungsgewalt.</p>
