    <p>Das laufende SNF-Projekt &laquo;N&uuml;tzliche Wissenschaft, Naturaneignung und Politik. Die Oekonomische Gesellschaft Bern im europ&auml;ischen Kontext, 1750&ndash;1850&raquo; (<a href="http://www.oeg.hist.unibe.ch" target="_blank">www.oeg.hist.unibe.ch</a>) am Historischen Institut der Universit&auml;t Bern untersucht die Reformbewegung der &ouml;konomischen Patrioten und fragt nach Generierung, Kommunikation, Umsetzung und Folgen neuer &laquo;n&uuml;tzlicher&raquo; Wissensbest&auml;nde. Albrecht von Haller ist einer jener f&uuml;r die Oekonomische Gesellschaft charakteristischen Akteure, die gleichzeitig als wissenschaftliche Fachexperten, als Verwaltungsleute und als Regierungsmitglieder agieren. Haller wird dabei weniger als singul&auml;re Erscheinung, sondern in vergleichender Perspektive zusammen mit anderen gelehrten Magistraten wie Samuel Engel, Daniel Fellenberg, Emanuel von Graffenried von Burgistein, Niklaus Emanuel Tscharner und Vinzenz Bernhard Tscharner betrachtet. <a href="mailto: martin.stuber@hist.unibe.ch">Martin Stuber</a></p>
<p>Im SNF-Projekt &laquo;Arzt und Patient um 1730&raquo; wird das vom noch nicht 30j&auml;hrigen Haller 1731&ndash;1736 w&auml;hrend seiner &auml;rztlichen Praxis in Bern detailliert in lateinischer Sprache gef&uuml;hrte Journal untersucht und ediert. Das Projekt ist vom Befund geleitet, dass wir trotz allen sozialgeschichtlichen Bem&uuml;hungen &uuml;ber den konkreten medizinischen Alltag fr&uuml;herer Epochen noch immer l&uuml;ckenhaft informiert sind. Dies gilt auch f&uuml;r die &auml;rztliche Praxis als zentralem Ort der Interaktion zwischen Arzt und Patient, an dem sich entscheidende soziale Prozesse und Strukturen festmachen lassen. Die Praxis wird sowohl aus &auml;rztlicher wie aus Patientensicht beleuchtet. Dadurch sollen neue Erkenntnisse &uuml;ber das soziale Gef&uuml;ge und Verhalten der Patientenschaft, die dem Arzt vorgelegten Leiden, die vorgenommenen Massnahmen und die f&uuml;r das Handeln wegleitenden Konzepte gewonnen werden. <a href="mailto:urs.boschung@mhi.unibe.ch">Urs Boschung</a></p>
<p>Im Jubil&auml;umsjahr 2008 fand die Tagung &laquo;Praktiken des Wissens und die Figur des Gelehrten im 18. Jahrhundert statt&raquo;. Das Tagungsprogramm finden Sie <a href="../medien/pdf/Praktiken_2008_Programm.pdf">hier</a>, die Abstracts <a href="../medien/pdf/Praktiken_2008_abstracts.pdf">hier</a>. Die Publikation der Tagungsakten ist in Arbeit. <a href="mailto:hubert.steinke@mhi.unibe.ch">Hubert Steinke</a></p>
<p>Ein zentrales Charakteristikum moderner Wissenschaft ist ihr Prozesscharakter. Wissenschaftliche Forschung findet nicht als einmaliger und individueller Akt der Wahrheitsfindung statt, sondern als Kontinuum von Bem&uuml;hungen in vielf&auml;ltigem Zusammenspiel von Personen und Institutionen. F&uuml;r diesen neuen dynamische Wissenschaftsmodus, der sich im 18. Jahrhundert durchzusetzen beginnt und der bis heute bestimmend bleibt, gilt G&ouml;ttingen als paradigmatischer Fall, namentlich im Zusammenspiel zwischen institutionellem Netzwerk - Universit&auml;t, Universit&auml;tsbibliothek, G&ouml;ttingische Gelehrte Anzeigen (GGA), Societ&auml;t/Gesellschaft der Wissenschaften (GdW) &ndash; und personenzentrierten Netzen herausragender Akteure wie Gerlach Adolph von M&uuml;nchhausen, Christian Gottlieb Heyne und Albrecht von Haller. In diesem Kontext wird zur Zeit in Kooperation zwischen verschiedenen Institutionen in Bern und G&ouml;ttingen ein Forschungsprojekt vorbereitet. <a href="mailto:martin.stuber@hist.unibe.ch">Martin Stuber</a></p>
